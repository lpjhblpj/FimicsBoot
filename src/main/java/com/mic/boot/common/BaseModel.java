package com.mic.boot.common;

public class BaseModel {

    private Object data;

    private int code;

    private String msg;

    public BaseModel(Object data, int code, String msg) {
        this.data =data;
        this.code = code;
        this.msg = msg;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
